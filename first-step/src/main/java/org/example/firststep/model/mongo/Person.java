package org.example.firststep.model.mongo;

import lombok.Getter;
import lombok.Setter;

@Getter
@Setter
public class Person {

    private String id;
    private String name;
    private int age;

    public Person(String name, int age) {
        this.name = name;
        this.age = age;
    }

    @Override
    public String toString() {
        return "Person [id=" + id + ", name=" + name + ", age=" + age + "]";
    }
}